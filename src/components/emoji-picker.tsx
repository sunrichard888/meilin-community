export default function EmojiPicker({ onSelect }: EmojiPickerProps) {
  const [selectedCategory, setSelectedCategory] = useState<keyof typeof EMOJI_CATEGORIES>("常用");
  const [isOpen, setIsOpen] = useState(false);

  return (
    <div className="relative">
      {/* 表情按钮 */}
      <button
        type="button"
        onClick={() => setIsOpen(!isOpen)}
        className="p-2 hover:bg-muted rounded-lg transition-colors"
        title="插入表情"
      >
        😊
      </button>

      {/* 表情选择器 */}
      {isOpen && (
        <div className="absolute bottom-full left-0 mb-2 bg-card border rounded-lg shadow-lg w-80 max-h-96 overflow-hidden z-50">
          {/* 分类标签 */}
          <div className="flex border-b overflow-x-auto scrollbar-hide">
            {Object.keys(EMOJI_CATEGORIES).map((category) => (
              <button
                key={category}
                onClick={() => setSelectedCategory(category as keyof typeof EMOJI_CATEGORIES)}
                className={`px-3 py-2 text-sm whitespace-nowrap transition-colors ${
                  selectedCategory === category
                    ? "bg-primary text-primary-foreground"
                    : "hover:bg-muted"
                }`}
              >
                {category}
              </button>
            ))}
          </div>

          {/* 表情网格 */}
          <div className="p-2 grid grid-cols-8 gap-1 overflow-y-auto max-h-64">
            {EMOJI_CATEGORIES[selectedCategory as keyof typeof EMOJI_CATEGORIES].map((emoji) => (
              <button
                key={emoji}
                type="button"
                onClick={() => {
                  onSelect(emoji);
                  setIsOpen(false);
                }}
                className="w-8 h-8 flex items-center justify-center hover:bg-muted rounded transition-colors text-lg"
              >
                {emoji}
              </button>
            ))}
          </div>
        </div>
      )}
    </div>
  );
}
